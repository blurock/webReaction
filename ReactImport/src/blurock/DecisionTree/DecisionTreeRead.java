    private void startButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_startButtonMouseClicked
        String filename = fileField.getText();
        try {
        RegisteredClasses registered = new RegisteredClasses();
        RWManager rw = new RWManager(registered);
        rw.openManager(filename,true);
        DataObjectClass treecls = registered.findClass("DecisionTree");
        BaseDataDecisionTree tree = new BaseDataDecisionTree();
        tree.Read(rw);

        DecisionTreeDrawPanel treepanel = new DecisionTreeDrawPanel(tree,registered);
        showPanel.add(treepanel,java.awt.BorderLayout.CENTER);
        show();
        } catch(IOException io) {
            System.out.println("Decision Tree read file error" + io);
        } catch(ObjectNotFoundException io) {
            System.out.println("Decision Tree not registered");
        }
    }//GEN-LAST:event_startButtonMouseClicked
