    private void update() {
        String[] parameters = speciesSets.getParameterNames();
        double[][] values = speciesSets.getMatrixValues();
        String[] setnames = speciesSets.getSetNames();
        
        BaseDataSetOfObjects spc = (BaseDataSetOfObjects) conditionsClass.SpeciesClass.BaseDataObjectExample();
        spc.Name = setnames[0];
        for(int j=0;j<parameters.length;j++) {
                BaseDataReal obj = (BaseDataReal) conditionsClass.SpeciesValueClass.BaseDataObjectExample();
                obj.Name = parameters[j];
                obj.realValue = values[0][j];
                spc.AddObject(obj);
            }
        conditions.Species = spc;
        try {
          Double t = new Double(deltatField.getText());
          conditions.DeltaT.realValue = t.doubleValue();
        } catch(NumberFormatException ex) {
            //do nothing, just don't save
        }
        try {
          Double p = new Double(pressureField.getText());
          conditions.Pressure.realValue = p.doubleValue();

        } catch(NumberFormatException ex) {
            //do nothing, just don't save
        }
        try {
          Double t = new Double(tempField.getText());
          conditions.Temperature.realValue = t.doubleValue();
        } catch(NumberFormatException ex) {
            //do nothing, just don't save
        }
    }    
    
