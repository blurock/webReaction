    public boolean fillInAttribute(String name) {
        boolean ans = param.getParameterAsString(name);
        objectText.setText(param.outputString);
        objectName.setText(name);
        objectClass.setText(param.attributeType);
        objectType.setText("Attribute");
        return ans;
    }
    public void inFrame(String name) {
        fillInAttribute(name);
        BaseFrame fr = new BaseFrame(this,name,name,"");
        fr.show();
    }
